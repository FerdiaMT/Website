function distance(ax, ay, bx, by, ang) {
  return sqrt((bx - ax) * (bx - ax) + (by - ay) * (by - ay));
}
function drawRays2D() {
  let r, mx, my, mp, dof;
  let rx, ry, ra, xo, yo;

  ra = pa - DEGREE * fov / 2;
  if (ra < 0) {
    ra += 2 * Math.PI;
  }
  if (ra > 2 * Math.PI) {
    ra -= 2 * Math.PI;
  }

  for (r = 0; r < fov; r++) {
    let vmt = 0; 
    let hmt = 0;
    // Check horizontal lines
    dof = 0;
    let disH = 1000000, hx = px, hy = py;
    let aTan = -1/Math.tan(ra);
    if (ra > Math.PI) {
      ry = ((int(py) >> 6) << 6) - 0.0001;
      rx = (py - ry) * aTan + px;
      yo = -64;
      xo = -yo * aTan;
    }
    if (ra < Math.PI) {
      ry = ((int(py) >> 6) << 6) + 64;
      rx = (py - ry) * aTan + px;
      yo = 64;
      xo = -yo * aTan;
    }
    if (ra == 0 || ra == Math.PI) {
      rx = px;
      ry = py;
      dof = 8;
    }
    while (dof < 8) {
      mx = int(rx) >> 6;
      my = int(ry) >> 6;
      mp = my * mapX + mx;

      if (mp > 0 && mp < mapX * mapY &&  mapW[mp] >0) {
        hmt = mapW[mp]-1;
        hx = rx;
        hy = ry;
        disH = distance(px, py, hx, hy, ra);
        dof = 8;
      } else {
        rx += xo;
        ry += yo;
        dof += 1;
      }
    }
    // Check vertical lines
    dof = 0;
    let disV = 1000000, vx = px, vy = py;
    let nTan = -1* (Math.tan(ra));
    if (ra > HALFPI && ra < THREEPI) {
      rx = ((int(px) >> 6) << 6) - 0.0001;
      ry = (px - rx) * nTan + py;
      xo = -64;
      yo = -xo * nTan;
    }
    if (ra < HALFPI || ra > THREEPI) {
      rx = ((int(px) >> 6) << 6) + 64;
      ry = (px - rx) * nTan + py;
      xo = 64;
      yo = -xo * nTan;
    }
    if (ra == 0 || ra == Math.PI) {
      rx = px;
      ry = py;
      dof = 8;
    }
    while (dof < 8) {
      mx = int(rx) >> 6;
      my = int(ry) >> 6;
      mp = my * mapX + mx;

      if (mp < mapX * mapY && mp > 0 && mapW[mp] >0) {
        vx = rx;
        vy = ry;
        disV = distance(px, py, vx, vy, ra);
        vmt = mapW[mp]-1;
        dof = 8;
      } else {
        rx += xo;
        ry += yo;
        dof += 1;
      }
    }
    let shade =1 ;
    if (disV < disH) {
      hmt = vmt;
      rx = vx;
      ry = vy;
      disT = disV;
      shade = 0.5;
    }
    if (disH < disV) {
      rx = hx;
      ry = hy;
      disT = disH;
      
    }



    let ca = pa - ra;
    disT=disT*Math.cos((ca))
    if (ca < 0) {
      ca += 2 * Math.PI;
    }
    if (ca > 2 * Math.PI) {
      ca -= 2 * Math.PI;
    }

    let lineH = (mapS * HEIGHT) / disT;
    let ty_step= (32 / lineH);
    let ty_off = 0;
    if (lineH > HEIGHT) {
      ty_off=(lineH-HEIGHT)/2;
      lineH = HEIGHT;
    }
    let lineO = 160 - (lineH>>1);
    //rendering the raycaster

    strokeWeight(8);
    strokeCap(SQUARE);
    
    let ty = ty_off*ty_step+(hmt*32);
    let tx = 0 ;
    if(shade ===1){
      tx = Math.floor((rx/2)%32);
      if (ra> Math.PI){
        tx = 31-tx;
      }
    }else{
      tx = Math.floor((ry/2)%32);
      if (ra>Math.PI/2 && ra < (Math.PI*(3/2))){
        tx = 31-tx;
      }
    }
    
    for(let y=0;y<lineH;y++){
      //line(r * 8 + 530, lineO, r * 8 + 530, lineH + lineO);
      
      let c = All_Textures[(int)(ty)*32 + tx]*shade;
      
      if(hmt === 0 )stroke(0,0,c*100); //checkboard test
      if(hmt === 1 )stroke(c*230,c*90,30 + c*60); // brick
      if(hmt === 2 )stroke(c*250,c*250,250); // window
      if(hmt === 3 )stroke(c*200,c*200,c*250,); // door
      if(hmt === 4 )stroke(c*250,0,0);
      
      line(r * 8, lineO+y, r * 8, lineO+y+1);
      ty+=ty_step;
    
    }
    stroke(50)
    line(r * 8, lineO+lineH, r * 8, lineO+lineH+HEIGHT);
    ra += DEGREE;
    if (ra < 0) {
      ra += 2 * Math.PI;
    }
    if (ra > 2 * Math.PI) {
      ra -= 2 * Math.PI;
    }
  }
}
